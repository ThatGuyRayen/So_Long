#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	num;

	if (n == INT_MIN)
	{
		write (fd, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		write (fd, "-", 1);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd (n / 10, fd);
	num = n % 10 + '0';
	write (fd, &num, 1);
}
